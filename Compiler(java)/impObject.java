package lexicalAnalyzer;

public class impObject {

	private String var, val;
	
	public impObject()
	{
		
	}
	public impObject(String var, String val)
	{
		this.var = var;
		this.val = val;
	}
	
	
	public String getVar() {
		return var;
	}

	public void setVar(String var) {
		this.var = var;
	}
	public String getVal() {
		return val;
	}

	public void setVal(String val) {
		this.val = val;
	}
	
	
}
